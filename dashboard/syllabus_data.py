def get_subjects_by_year_sem(year, sem, department="IT"):
    """Get subjects for a specific year-semester and department"""
    # Since we only have IT department now, we ignore the department parameter
    key = f"{year}-{sem}"
    return SYLLABUS_DATA.get(key, [])


def get_all_subjects():
    """Get all subjects across all departments and semesters"""
    all_subjects = []
    for key, subjects in SYLLABUS_DATA.items():
        for subject in subjects:
            all_subjects.append({
                'year_sem': key,
                'code': subject['code'],
                'name': subject['name']
            })
    return all_subjects


def get_subjects_by_type(year_sem, subject_type=None):
    """Get subjects filtered by type (PC, PE, OE, etc.)"""
    subjects = SYLLABUS_DATA.get(year_sem, [])

    if not subject_type:
        return subjects

    filtered_subjects = []
    for subject in subjects:
        if subject_type == "PC" and subject['code'].endswith('PC'):
            filtered_subjects.append(subject)
        elif subject_type == "PE" and subject['code'].endswith('PE'):
            filtered_subjects.append(subject)
        elif subject_type == "OE" and subject['code'].endswith('OE'):
            filtered_subjects.append(subject)
        elif subject_type == "PW" and subject['code'].endswith('PW'):
            filtered_subjects.append(subject)
        elif subject_type == "LAB" and any(x in subject['name'].upper() for x in ['LABORATORY', 'LAB']):
            filtered_subjects.append(subject)

    return filtered_subjects


def get_subject_by_code(subject_code):
    """Get subject details by code across all semesters"""
    for year_sem, subjects in SYLLABUS_DATA.items():
        for subject in subjects:
            if subject['code'] == subject_code:
                return {
                    'year_sem': year_sem,
                    'code': subject['code'],
                    'name': subject['name']
                }
    return None


def get_subjects_by_department(department="IT"):
    """Get all subjects for a specific department"""
    # Currently only IT department is supported
    if department.upper() != "IT":
        return []

    all_subjects = []
    for year_sem, subjects in SYLLABUS_DATA.items():
        for subject in subjects:
            all_subjects.append({
                'year_sem': year_sem,
                'code': subject['code'],
                'name': subject['name']
            })
    return all_subjects


def get_year_sem_list():
    """Get list of all year-semester combinations"""
    return list(SYLLABUS_DATA.keys())


def search_subjects(search_term):
    """Search subjects by code or name"""
    search_term = search_term.upper()
    results = []

    for year_sem, subjects in SYLLABUS_DATA.items():
        for subject in subjects:
            if (search_term in subject['code'] or
                    search_term in subject['name'].upper()):
                results.append({
                    'year_sem': year_sem,
                    'code': subject['code'],
                    'name': subject['name']
                })

    return results